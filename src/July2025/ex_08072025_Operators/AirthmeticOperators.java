package July2025.ex_08072025_Operators;

public class AirthmeticOperators {
    public static void main(String[] args) {
        int a=20;
        int b=10;
        /*
        * + > addition
        * - > substraction
        * * multiplication
        * / division
        * % modulation
        * */
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
