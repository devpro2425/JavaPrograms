package July2025.ex_10072025_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        //+,-,*,/,%
        int a=20;
        int b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);//moudules
    }
}
