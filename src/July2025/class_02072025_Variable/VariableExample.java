package July2025.class_02072025_Variable;

public class VariableExample {
    public static void main(String[] args) {
        int age=65;
        age=92; //can define a variable with different values once it is allocated before.
        //int -> data type (whole number)
        // age -> variable name/Identifier
        // = -> assignment operator
        // 65 -> variable values
        System.out.println(age);

    }
}
